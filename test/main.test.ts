import * as std_md from './std_md'

describe('2. Markdown standard rule test', () => {
  // ===================================================================================================================
  // Heading tag
  // ===================================================================================================================

  test('# -> <h1>', () => {
    std_md.h1_test('#')
  })

  test('===== -> <h1>', () => {
    std_md.h1_test('=')
  })

  test('## -> <h2>', () => {
    std_md.h2_test('#')
  })

  test('----- -> <h2>', () => {
    std_md.h2_test('-')
  })

  // ===================================================================================================================
  // Italic tag
  // ===================================================================================================================

  test('* -> <i>', () => {
    std_md.italic_test('*')
  })

  test('_ -> <i>', () => {
    std_md.italic_test('_')
  })

  // ===================================================================================================================
  // Bold tag
  // ===================================================================================================================

  test('** -> <b>', () => {
    std_md.bold_test('**')
  })

  test('__ -> <b>', () => {
    std_md.bold_test('__')
  })

  // ===================================================================================================================
  // Ul tag or Ol tag
  // ===================================================================================================================

  test('- -> <ul>', () => {
    std_md.ul_test()
  })

  // ===================================================================================================================
  // Mix test
  // ===================================================================================================================

  test('***A*** -> <i><b>A</b></i>', () => {
    std_md.mix_test_001()
  })

  test('*A**B*** -> <i>A<b>B</b></i>', () => {
    std_md.mix_test_002()
  })

  test('**A*B*** -> <b>A<i>B</i></b>', () => {
    std_md.mix_test_003()
  })

  test('***A*B** -> <b><i>A</i>B</b>', () => {
    std_md.mix_test_004()
  })

  test('***A**B* -> <i><b>A</b>B</i>', () => {
    std_md.mix_test_005()
  })
})
